def get_flag_data():
    response = requests.get(COUNTRY_DATA_URL)
    country_data = response.json()

    flag_data = {}

    for country in country_data.values():
        flag = country["extra"]["emoji"]
        flag_data[country["name"]["common"]] = flag
        flag_data[country["name"]["official"]] = flag
        flag_data[country["demonym"]] = flag

    return flag_data


